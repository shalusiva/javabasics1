package com.basics1;

public class switchcase {
public static void main(String arg[])
{
int choice=10;
switch(choice)
{
case 10:
	System.out.println(10);
	break;
case 20:
	System.out.println(20);
	break;
case 30:
	System.out.println(30);
	break;
default:
	System.out.println("default");
	break;
}
}

}
